function textToAscii(text) {
    const upper = text.toUpperCase();
    const lines = ['', '', '', '', ''];

    for (let i = 0; i < upper.length; i++) {
        const ch = upper[i];
        const glyph = FONT[ch] || FONT['?'];
        const space = FONT[' '];

        for (let row = 0; row < 5; row++) {
            if (i > 0) {
                lines[row] += ' ';
            }
            lines[row] += glyph[row];
        }
    }

    return '```\n' + lines.join('\n') + '\n```';
}

module.exports = {
    name: 'ascii',
    aliases: ['asciiart', 'textart'],
    description: 'Convert text to ASCII art',
    usage: 'ascii <text>',
    async execute(message, args) {
        const text = args.join(' ');
        if (!text) {
            return message.reply('ÔŁî Please provide some text!\n**Usage:** `ascii <text>`');
        }
        if (text.length > 20) {
            return message.reply('ÔŁî Text too long! Maximum **20 characters** allowed.');
        }

        const art = textToAscii(text);
        await message.reply(art);
    }
};